%#####################################################################################################
%# Function  : hat operation ( sxw = hat(s)ws)   [hat(s) is a 3x3 matrix]                            #   
%#                                                                                                   #       
%# Input(s)  : vector of length 3                                                                    #
%#                                                                                                   #                
%# Ouptut(s) : hat of the vector                                                                     # 
%#                                                                                                   #       
%# Example: hat([1,2,3])                                                                             #   
%#                                                                                                   #       
%#                                                                                                   #               
%#                                                                                                   #                                       
%#####################################################################################################

function [v_hat] = hat(v)

v_hat = [0     -v(3)  v(2);
         v(3)   0    -v(1);
        -v(2)   v(1)   0  ];

end

